<?php

namespace App\Repositories;

use App\Helpers\ImageHelper;
use App\Team;

class TeamRepository
{
    public function __construct()
    {
        //
    }


    
    /**
     * Store meal.
     *
     * @param  \App\Http\Requests\ImageStoreRequest  $request
     * @return void
     */

    public function store($request)
    {
        $input = $request->all();
        $input['photo'] = ImageHelper::handleUploadedImage($request->file('photo'),'assets/images');
        Team::create($input);
    }

    /**
     * Update Brand.
     *
     * @param  \App\Http\Requests\ImageUpdateRequest  $request
     * @return void
     */

    public function update($team, $request)
    {
        $input = $request->all();
        if ($file = $request->file('photo')) {
            $input['photo'] = ImageHelper::handleUpdatedUploadedImage($file,'/assets/images',$team,'/assets/images/','photo');

        }
        $team->update($input);
    }

    /**
     * Delete brand.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */

    public function delete($team)
    {
        ImageHelper::handleDeletedImage($team,'photo','assets/images/');
        $team->delete();
    }
}
